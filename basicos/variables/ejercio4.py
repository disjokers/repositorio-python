# Declarar variables de diferentes tipos
numero_entero = 42           # int
numero_decimal = 3.14        # float
cadena_texto = "Hola"        # str
valor_booleano = True        # bool

# Imprimir el valor y el tipo de cada variable
print("Valor:", numero_entero, "| Tipo:", type(numero_entero))
print("Valor:", numero_decimal, "| Tipo:", type(numero_decimal))
print("Valor:", cadena_texto, "| Tipo:", type(cadena_texto))
print("Valor:", valor_booleano, "| Tipo:", type(valor_booleano))
